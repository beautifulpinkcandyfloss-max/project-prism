WHAT_IS_CHANNELING = """
**Channeling** is the practice of a person (the "channel" or "instrument") entering a
receptive state of consciousness and relaying information that they describe as coming
from a source beyond their own ordinary awareness -- variously described by channels
themselves as a non-physical being, a group consciousness, a higher aspect of self, or an
intelligence from another density of reality.

It takes different forms across the sources in this archive: some channels speak in a
light trance while fully aware (Carla Rueckert channeling Ra and Q'uo), others write or
type automatically, others use a Ouija board with a small group, and others describe
receiving the material in an altered but conscious state while awake.

Project Prism takes no position on whether these are genuinely contacts with non-physical
intelligences, productions of the channel's subconscious, or something else entirely --
that's a question for you to explore. What we *can* say with confidence is that these are
real documents, with real dates, real participants, and a real, traceable history -- and
that's what this archive lets you examine directly, source by source, rather than through
secondhand summary.
"""

